#include <unistd.h>

void	ft_putendl_fd(char *c, int fd)
{
	int	i;

	if (!c)
		return ;
	i = 0;
	while (c[i])
	{
		write(fd, &c[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}
